# Find number is even or odd

numbers = [1, 2, 3, 4, 5, 6]

for number in numbers:
    if number%2 == 0 : 
        print("Number :",number," is Even")
    else :
        print("Number :",number," is Odd")
    


# Number : 1  is Odd
# Number : 2  is Even
# Number : 3  is Odd
# Number : 4  is Even
