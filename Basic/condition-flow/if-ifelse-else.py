
# Confitional Flow example : if, elif, else

age =110

if age < 18 :
    print("You age is :",age)
    print("You are minor !!")
elif age >= 18 and age <= 60 :
    print("You age is :",age)
    print("You are adult !!")
else :
    print("You age is :",age)
    print("You are senior-citizen !!")

# You age is : 110
# You are senior-citizen !!

